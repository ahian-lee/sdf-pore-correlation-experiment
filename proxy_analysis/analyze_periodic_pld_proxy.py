import argparse
import json
from pathlib import Path
from typing import Tuple

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd


def parse_args():
    parser = argparse.ArgumentParser(
        description="Analyze PLD proxy results with diagnostics plots and simple calibration."
    )
    parser.add_argument(
        "--results-csv",
        type=Path,
        required=True,
        help="CSV generated by compute_periodic_pld_proxy.py",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        required=True,
        help="Directory to save figures and summaries.",
    )
    return parser.parse_args()


def regression_metrics(y_true: np.ndarray, y_pred: np.ndarray) -> dict:
    diff = y_pred - y_true
    ss_res = float(np.sum(np.square(diff)))
    ss_tot = float(np.sum(np.square(y_true - np.mean(y_true))))
    return {
        "mae": float(np.mean(np.abs(diff))),
        "rmse": float(np.sqrt(np.mean(np.square(diff)))),
        "bias": float(np.mean(diff)),
        "pearson": float(pd.Series(y_true).corr(pd.Series(y_pred), method="pearson")),
        "spearman": float(pd.Series(y_true).corr(pd.Series(y_pred), method="spearman")),
        "r2": float(1.0 - ss_res / ss_tot) if ss_tot > 0 else float("nan"),
    }


def fit_linear_calibration(y_true: np.ndarray, y_proxy: np.ndarray) -> Tuple[float, float]:
    slope, intercept = np.polyfit(y_proxy, y_true, 1)
    return float(slope), float(intercept)


def save_scatter_plot(df: pd.DataFrame, output_path: Path):
    y_true = df["true_pld"].to_numpy()
    y_proxy = df["proxy_pld"].to_numpy()
    y_cal = df["proxy_pld_calibrated"].to_numpy()

    max_axis = float(max(y_true.max(), y_proxy.max(), y_cal.max()) + 0.5)

    fig, axes = plt.subplots(1, 2, figsize=(12, 5), constrained_layout=True)
    panels = [
        ("Raw Proxy", y_proxy, "#1f77b4"),
        ("Calibrated Proxy", y_cal, "#d62728"),
    ]

    for ax, (title, pred, color) in zip(axes, panels):
        ax.scatter(y_true, pred, s=18, alpha=0.7, color=color, edgecolors="none")
        ax.plot([0, max_axis], [0, max_axis], linestyle="--", color="black", linewidth=1)
        ax.set_title(title)
        ax.set_xlabel("True PLD (A)")
        ax.set_ylabel("Predicted PLD (A)")
        ax.set_xlim(0, max_axis)
        ax.set_ylim(0, max_axis)
        metrics = regression_metrics(y_true, pred)
        ax.text(
            0.04,
            0.96,
            f"MAE={metrics['mae']:.3f} A\nRMSE={metrics['rmse']:.3f} A\nR2={metrics['r2']:.3f}\nSpearman={metrics['spearman']:.3f}",
            transform=ax.transAxes,
            va="top",
            ha="left",
            fontsize=10,
            bbox={"facecolor": "white", "alpha": 0.8, "edgecolor": "#cccccc"},
        )

    fig.suptitle("Periodic PLD Proxy vs True PLD")
    fig.savefig(output_path, dpi=200)
    plt.close(fig)


def save_binned_plot(df: pd.DataFrame, output_path: Path):
    quantiles = np.quantile(df["true_pld"], [0.0, 0.25, 0.5, 0.75, 1.0])
    bins = np.unique(quantiles)
    if len(bins) < 3:
        bins = np.linspace(df["true_pld"].min(), df["true_pld"].max(), 5)

    df = df.copy()
    df["pld_bin"] = pd.cut(df["true_pld"], bins=bins, include_lowest=True, duplicates="drop")

    summary = (
        df.groupby("pld_bin", observed=True)
        .agg(
            count=("name", "size"),
            true_pld_mean=("true_pld", "mean"),
            raw_mae=("raw_abs_error", "mean"),
            raw_bias=("raw_signed_error", "mean"),
            cal_mae=("cal_abs_error", "mean"),
            cal_bias=("cal_signed_error", "mean"),
        )
        .reset_index()
    )

    centers = np.arange(len(summary))
    labels = [str(v) for v in summary["pld_bin"]]

    fig, axes = plt.subplots(1, 2, figsize=(13, 5), constrained_layout=True)
    width = 0.36

    axes[0].bar(centers - width / 2, summary["raw_mae"], width=width, color="#1f77b4", label="Raw")
    axes[0].bar(centers + width / 2, summary["cal_mae"], width=width, color="#d62728", label="Calibrated")
    axes[0].set_title("MAE by True PLD Bin")
    axes[0].set_ylabel("MAE (A)")
    axes[0].set_xticks(centers)
    axes[0].set_xticklabels(labels, rotation=20, ha="right")
    axes[0].legend()

    axes[1].bar(centers - width / 2, summary["raw_bias"], width=width, color="#1f77b4", label="Raw")
    axes[1].bar(centers + width / 2, summary["cal_bias"], width=width, color="#d62728", label="Calibrated")
    axes[1].axhline(0, color="black", linestyle="--", linewidth=1)
    axes[1].set_title("Bias by True PLD Bin")
    axes[1].set_ylabel("Bias (A)")
    axes[1].set_xticks(centers)
    axes[1].set_xticklabels(labels, rotation=20, ha="right")
    axes[1].legend()

    fig.suptitle("PLD Proxy Error Across True PLD Ranges")
    fig.savefig(output_path, dpi=200)
    plt.close(fig)

    summary.to_csv(output_path.with_suffix(".csv"), index=False)


def main():
    args = parse_args()
    args.output_dir.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(args.results_csv)
    y_true = df["true_pld"].to_numpy()
    y_proxy = df["proxy_pld"].to_numpy()

    slope, intercept = fit_linear_calibration(y_true, y_proxy)
    y_cal = slope * y_proxy + intercept

    df["proxy_pld_calibrated"] = y_cal
    df["raw_abs_error"] = np.abs(df["proxy_pld"] - df["true_pld"])
    df["raw_signed_error"] = df["proxy_pld"] - df["true_pld"]
    df["cal_abs_error"] = np.abs(df["proxy_pld_calibrated"] - df["true_pld"])
    df["cal_signed_error"] = df["proxy_pld_calibrated"] - df["true_pld"]

    raw_metrics = regression_metrics(y_true, y_proxy)
    cal_metrics = regression_metrics(y_true, y_cal)

    metrics = {
        "raw": raw_metrics,
        "calibrated": cal_metrics,
        "linear_calibration": {
            "slope": slope,
            "intercept": intercept,
        },
    }

    analyzed_csv = args.output_dir / "pld_proxy_results_with_calibration.csv"
    metrics_json = args.output_dir / "calibration_metrics.json"
    scatter_png = args.output_dir / "pld_proxy_true_vs_pred.png"
    binned_png = args.output_dir / "pld_proxy_binned_error.png"

    df.to_csv(analyzed_csv, index=False)
    metrics_json.write_text(json.dumps(metrics, indent=2))
    save_scatter_plot(df, scatter_png)
    save_binned_plot(df, binned_png)

    print(json.dumps(metrics, indent=2))
    print(f"Saved calibrated results to {analyzed_csv}")
    print(f"Saved metrics to {metrics_json}")
    print(f"Saved scatter plot to {scatter_png}")
    print(f"Saved binned plot to {binned_png}")


if __name__ == "__main__":
    main()
